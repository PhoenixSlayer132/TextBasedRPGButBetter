namespace TextBasedRPG.BackEnd;

public class PlayerStats
{
    private int hp;
    private int str;
    private int dex;
    private int intel;
    private int wis;
    private int spd;
    
    public PlayerStats(int health, int strength, int dexterity, int intelligence, int wisdom, int speed)
    {
        hp = health;
        str = strength;
        dex = dexterity;
        intel = intelligence;
        wis = wisdom;
        spd = speed;
    }
    
    
    
    
}